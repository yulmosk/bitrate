import UIKit

extension UIFont {
    public class func latoRegular(size: CGFloat) -> UIFont! {
        return UIFont(name: "Lato-Regular", size: size)
    }
    public class func latoBold(size: CGFloat) -> UIFont! {
        return UIFont(name: "Lato-Bold", size: size)
    }
}

